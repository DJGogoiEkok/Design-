document.addEventListener('DOMContentLoaded', () => {
    const mediaItems = document.querySelectorAll('#hero-media-container .hero-slide');
    if (mediaItems.length <= 1) {
        // If there is only 1 video, make it loop
        if (mediaItems.length === 1 && mediaItems[0].tagName.toLowerCase() === 'video') {
            mediaItems[0].loop = true;
        }
        return; 
    }

    let currentIndex = 0;

    function showNextMedia() {
        // Hide current
        mediaItems[currentIndex].style.display = 'none';
        if (mediaItems[currentIndex].tagName.toLowerCase() === 'video') {
            mediaItems[currentIndex].pause();
            mediaItems[currentIndex].currentTime = 0;
        }

        // Show next
        currentIndex = (currentIndex + 1) % mediaItems.length;
        mediaItems[currentIndex].style.display = 'block';

        if (mediaItems[currentIndex].tagName.toLowerCase() === 'video') {
            mediaItems[currentIndex].play().catch(e => console.log('Autoplay prevented', e));
        } else {
            // If it's an image, wait 5 seconds then show next
            setTimeout(showNextMedia, 5000);
        }
    }

    // Attach event listeners to all videos to transition when ended
    mediaItems.forEach((item, idx) => {
        if (item.tagName.toLowerCase() === 'video') {
            item.addEventListener('ended', showNextMedia);
            item.loop = false; // ensure it doesn't loop so it fires 'ended'
        }
    });

    // If the first item is an image, start the timer
    if (mediaItems[0].tagName.toLowerCase() !== 'video') {
        setTimeout(showNextMedia, 5000);
    }
});
</script>
